#!/usr/bin/env python3
"""Build wikidata-subset.ttl: the minimal Wikidata data needed to answer the
example question ("Who is the inventor of the Hawaiian Pizza?") with the SPARQL
query generated by QB-Wikidata. Data is copied from Wikidata via the public
Action API (https://www.wikidata.org/w/api.php), not the throttled WDQS.

The QB query joins, for the subject entity:
  ?subject rdfs:label ?label ; ?p ?o . ?o rdfs:label ?oLabel .
  ?prop wikibase:directClaim ?p ; rdfs:label ?pLabel .  (all @en)
so we need: the subject label, its entity-valued truthy statements, the object
labels, and for each used property its wikibase:directClaim + label."""
import json, sys, urllib.parse, urllib.request

UA = "Qanary-Minimal-Example/1.0 (https://github.com/WSE-research/Qanary_minimal_Python_example) dataset-build"
API = "https://www.wikidata.org/w/api.php"
SUBJECTS = ["Q590076"]  # Hawaiian pizza

def api(params):
    params = {**params, "format": "json"}
    url = API + "?" + urllib.parse.urlencode(params)
    req = urllib.request.Request(url, headers={"User-Agent": UA})
    with urllib.request.urlopen(req, timeout=30) as r:
        return json.load(r)

def get_entities(ids, props, **extra):
    out = {}
    for i in range(0, len(ids), 50):
        chunk = ids[i:i+50]
        d = api({"action": "wbgetentities", "ids": "|".join(chunk), "props": props, **extra})
        out.update(d.get("entities", {}))
    return out

# 1) subject claims -> entity-valued truthy statements
subj = get_entities(SUBJECTS, "claims|labels", languages="en")
statements = []   # (subject, pid, objectQid)
props, objs = set(), set()
for sid in SUBJECTS:
    for pid, arr in subj[sid]["claims"].items():
        for st in arr:
            try:
                dv = st["mainsnak"]["datavalue"]
                if dv["type"] == "wikibase-entityid" and st["mainsnak"]["snaktype"] == "value":
                    qid = dv["value"]["id"]
                    statements.append((sid, pid, qid)); props.add(pid); objs.add(qid)
            except Exception:
                pass

# 2) labels for subjects + objects, and labels for properties
ent_labels = get_entities(sorted(set(SUBJECTS) | objs), "labels", languages="en")
prop_labels = get_entities(sorted(props), "labels", languages="en")

def label(entities, _id):
    return entities.get(_id, {}).get("labels", {}).get("en", {}).get("value")

# 3) emit turtle
lines = [
    "# Wikidata subset for the Qanary_minimal_Python_example test question(s).",
    "# Generated by build-dataset.py from the Wikidata Action API (real data).",
    "@prefix wd: <http://www.wikidata.org/entity/> .",
    "@prefix wdt: <http://www.wikidata.org/prop/direct/> .",
    "@prefix wikibase: <http://wikiba.se/ontology#> .",
    "@prefix rdfs: <http://www.w3.org/2000/01/rdf-schema#> .",
    "",
]
def esc(s): return s.replace("\\", "\\\\").replace('"', '\\"')

lines.append("# entity labels")
for qid in sorted(set(SUBJECTS) | objs):
    lab = label(ent_labels, qid)
    if lab:
        lines.append(f'wd:{qid} rdfs:label "{esc(lab)}"@en .')
lines.append("")
lines.append("# entity-valued truthy statements (subject ?p object)")
for s, p, o in statements:
    if label(ent_labels, o):  # only keep statements whose object has an @en label (the query needs it)
        lines.append(f'wd:{s} wdt:{p} wd:{o} .')
lines.append("")
lines.append("# property definitions: directClaim + label")
for pid in sorted(props):
    lab = label(prop_labels, pid)
    if lab:
        lines.append(f'wd:{pid} wikibase:directClaim wdt:{pid} ; rdfs:label "{esc(lab)}"@en .')
lines.append("")

ttl = "\n".join(lines)
with open("wikidata-subset.ttl", "w") as f:
    f.write(ttl)
print(ttl)
print(f"\n# {len(statements)} statements, {len(objs)} objects, {len(props)} properties", file=sys.stderr)
